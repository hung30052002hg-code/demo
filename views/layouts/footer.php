    <script>
        const chatToggle = document.getElementById('chatbotToggle');
        const chatWindow = document.getElementById('chatbotWindow');
        const chatClose = document.getElementById('chatbotClose');
        const chatInput = document.getElementById('chatbotInput');
        const chatSend = document.getElementById('chatbotSend');
        const chatMessages = document.getElementById('chatbotMessages');

        if (chatToggle && chatWindow) {
            chatToggle.addEventListener('click', () => {
                chatWindow.classList.toggle('hidden');
                chatMessages.scrollTop = chatMessages.scrollHeight;
            });
            if (chatClose) chatClose.addEventListener('click', () => chatWindow.classList.add('hidden'));
            
            chatSend.addEventListener('click', handleUserMessage);
            chatInput.addEventListener('keydown', (e) => {
                if (e.key === 'Enter') handleUserMessage();
            });
        }

        const currentUserName = '<?php echo isset($_SESSION['user_name']) ? htmlspecialchars($_SESSION['user_name']) : ""; ?>';

        function handleUserMessage() {
            const text = chatInput.value.trim();
            if (!text) return;
            
            appendMessage(text, 'user');
            chatInput.value = '';
            
            getAIResponse(text);
        }

        function sendQuickReply(text) {
            appendMessage(text, 'user');
            getAIResponse(text);
        }

        function getAIResponse(text) {
            const formData = new FormData();
            formData.append('message', text);

            fetch('<?php echo BASE_URL; ?>index.php?controller=home&action=chatbot', {
                method: 'POST',
                body: formData
            })
            .then(res => res.json())
            .then(data => {
                if (data.success && data.reply) {
                    appendMessage(data.reply, 'bot');
                } else {
                    const reply = getBotReply(text);
                    appendMessage(reply, 'bot');
                }
            })
            .catch(err => {
                console.error(err);
                const reply = getBotReply(text);
                appendMessage(reply, 'bot');
            });
        }

        function appendMessage(text, sender) {
            const msgRow = document.createElement('div');
            if (sender === 'user') {
                msgRow.className = 'flex justify-end';
                msgRow.innerHTML = `
                    <div class="bg-primary text-white rounded-2xl rounded-tr-none p-3 max-w-[85%] leading-normal">
                        ${text}
                    </div>
                `;
            } else {
                msgRow.className = 'flex items-start gap-2 max-w-[85%]';
                msgRow.innerHTML = `
                    <span class="text-xl">🤖</span>
                    <div class="bg-white/5 border border-white/5 rounded-2xl rounded-tl-none p-3 text-text-light leading-normal">
                        ${text}
                    </div>
                `;
            }
            chatMessages.appendChild(msgRow);
            chatMessages.scrollTop = chatMessages.scrollHeight;
        }

        function getBotReply(input) {
            const text = input.toLowerCase();
            
            // Rule-based bot logic
            if (text.includes('giờ') || text.includes('mở cửa') || text.includes('time')) {
                return '⏰ Cửa hàng CHUS TEA mở cửa đón khách từ <strong>07:00 AM đến 10:30 PM</strong> hằng ngày (kể cả cuối tuần và ngày lễ) bạn nhé!';
            }
            if (text.includes('địa chỉ') || text.includes('ở đâu') || text.includes('location')) {
                return '📍 CHUS TEA tọa lạc tại <strong>Ngõ 33 Xuân Thị Tứ, Minh Hải, Hải Phòng</strong>. Rất hân hạnh được đón tiếp bạn!';
            }
            if (text.includes('khuyến mãi') || text.includes('giảm giá') || text.includes('coupon') || text.includes('code')) {
                return '🎁 Hiện tại chúng tôi đang có các mã giảm giá hấp dẫn:<br>- <strong>STARBUCKS10</strong>: Giảm 10k đơn từ 50k<br>- <strong>CHUSTEAFREE</strong>: Giảm 20k đơn từ 100k<br>- <strong>WELCOME50</strong>: Giảm 50k đơn từ 200k<br>Hãy áp dụng tại trang thanh toán nhé!';
            }
            if (text.includes('ship') || text.includes('giao hàng') || text.includes('phí')) {
                return '🚀 CHUS TEA đang hỗ trợ **MIỄN PHÍ GIAO HÀNG** hoàn toàn cho tất cả các đơn hàng khu vực nội thành Quận 1 và lân cận! Thời gian giao từ 15-30 phút.';
            }
            if (text.includes('menu') || text.includes('trà sữa') || text.includes('sản phẩm')) {
                return '🧋 Menu của chúng tôi có Trà Sữa Truyền Thống, Trà Trái Cây, Đá Xay và Cà Phê. Bạn hãy vào mục <a href="index.php?controller=product&action=index" class="text-primary hover:underline font-bold">Thực Đơn</a> để xem giá và đặt món nhé!';
            }
            if (text.includes('hello') || text.includes('chào') || text.includes('hi')) {
                if (currentUserName) {
                    return `👋 Chào <strong>${currentUserName}</strong>! Mình có thể hỗ trợ gì cho bạn hôm nay?`;
                }
                return '👋 Xin chào! Mình có thể hỗ trợ gì cho bạn về menu, giờ mở cửa hay giao hàng không?';
            }
            
            return '🤖 Cảm ơn bạn đã hỏi. Câu hỏi này nằm ngoài cơ sở dữ liệu tự động. Nhân viên CSKH sẽ phản hồi bạn qua Livechat hoặc SĐT trong ít phút nữa nhé!';
        }
